
# Strings are immutable array of bytes representing Unicode characters
# Python has no char data type, a single character is simply a string with a length of 1
# Since strings are immutable, modifying a string results in creating a new copy

String = "This is a Data Structures and Algorithms repo"
print("Creating String: ")
print(String)

# Printing First Character of String
print("\nFirst character of string is: ")
print(String[0])

# Printing Last character of String
print("\nLast character of String is: ")
print(String[-1])
