
# Lists are ordered collectiosn of data just like arrays in other languages
# Allows different types of elements in the list
# Similar to ArrayLists and Vectors
# Costly operation when inserting or deleting elements due to the fact that elements need to be shifted

List = ["Geeks", "For", "Geeks"]
print("\nList containing multiple values")
print(List)

List2 = [['Geeks', 'For'], ['Geeks']]
print("\nMulti-Dimensional List")
print(List2)

print("Accessing element from the list")
print(List[0])
print(List[2])

print("\nAccessing element using negative indexing")
print(List[-1])
print(List[-3])
